int API_IO_putc(int c, int x_lup, int y_lup, int kleur)
{
	c = c-' '; //(die ' ', moest er nog af)
	int start = consolas_12ptDescriptors[c][1]; // (zal het wel anders noemen)
	int stop  = (c == TEKENS-1)? sizeof(consolas_12ptDescriptors):consolas_12ptDescriptors[c+1][1];
	int i,j,k;

	for(i=0; start<stop; start+=consolas_12ptDescriptors[c][0], i++)
	{
		for(k=0; k<consolas_12ptDescriptors[c][0]; k++)
		{
			for(j=0; j<BIT; j++) // brackets kunnen weg
				if(0x80>>j & consolas_12ptBitmaps[start+k])
					UB_VGA_SetPixel((BIT*k)+j+x_lup, y_lup, kleur); //R: Maakt "l" overbodig
				//else
					//UB_VGA_SetPixel((BIT*k)+j+x_lup, y_lup, VGA_COL_WHITE);
		}
		y_lup ++;
	}
	return 0;
}


